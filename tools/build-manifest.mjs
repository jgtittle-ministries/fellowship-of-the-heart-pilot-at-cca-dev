#!/usr/bin/env node
// =====================================================================
// build-manifest.mjs — generate manifest.js by scanning docs/.
// No hand-maintained chapter list. Run from the repo root:
//     node tools/build-manifest.mjs
// Re-run whenever content is added, removed, or retitled.
// Dependency-free (Node 16+). Writes ../manifest.js relative to this file.
// =====================================================================
import { readdirSync, writeFileSync, statSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { titleFromMd } from './titles.mjs';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const DOCS = join(ROOT, 'docs');

// ---- Site-level config (the only thing you hand-set) ----
const SITE = {
  title: 'Fellowship of the Heart',
  subtitle: 'CCA Pilot',
  envLabel: 'DEV',                 // '' for prod, 'DEV' for the dev repo (point 7)
  license: { label: 'CC BY-SA 4.0', url: 'https://creativecommons.org/licenses/by-sa/4.0/' },
  repo: 'https://github.com/jgtittle-ministries/fellowship-of-the-heart-pilot-at-cca-dev'
};

// Standalone root pages, in nav order, then series folders in nav order.
const ROOT_PAGES = ['index.md', 'start-here.md'];
const SERIES_DEFS = [
  { id: 'getting-started', name: 'Getting Started',   edition: 'v3' },
  { id: 'going-deeper',    name: 'Going Deeper',      edition: 'v2' },
  { id: 'going-out',       name: 'Going Out',         edition: 'v3' },
  { id: 'shared',          name: 'Shared materials',  edition: null }
];

// ---- helpers ----
// titleFromMd(file, rel) lives in ./titles.mjs (shared with build-search-index.mjs).

// session ordering within a series: index, handbook, *guide*, week-NN…, others, CHANGELOG last
const orderKey = (name) => {
  const n = name.toLowerCase();
  if (n === 'index.md') return [0, 0, n];
  if (n.includes('handbook')) return [1, 0, n];
  if (n.includes('guide')) return [2, 0, n];
  const wk = n.match(/week-(\d+)/);
  if (wk) return [3, parseInt(wk[1], 10), n];
  if (n.includes('changelog')) return [9, 0, n];
  return [5, 0, n];
};
const cmp = (a, b) => (a[0]-b[0]) || (a[1]-b[1]) || (a[2] < b[2] ? -1 : a[2] > b[2] ? 1 : 0);

const listMd = (dir) => {
  let out = [];
  for (const e of readdirSync(dir)) {
    const p = join(dir, e);
    if (statSync(p).isDirectory()) continue;
    if (e.endsWith('.md')) out.push(e);
  }
  return out;
};

// ---- build SERIES ----
const series = [];

// root pages
const pageMeta = { 'index.md': 'Home', 'start-here.md': 'Start here' };
for (const f of ROOT_PAGES) {
  const abs = join(DOCS, f);
  try { statSync(abs); } catch { continue; }
  series.push({ id: f.replace('.md',''), kind: 'page', name: pageMeta[f] || titleFromMd(abs, f), path: `docs/${f}` });
}

// series folders
for (const def of SERIES_DEFS) {
  const dir = join(DOCS, def.id);
  let files;
  try { files = listMd(dir); } catch { continue; }
  files.sort((a, b) => cmp(orderKey(a), orderKey(b)));
  const chapters = files.map(f => ({ path: `docs/${def.id}/${f}`, title: titleFromMd(join(dir, f), `${def.id}/${f}`) }));
  series.push({
    id: def.id, kind: 'series', name: def.name, folder: `docs/${def.id}`,
    edition: def.edition, index: `docs/${def.id}/index.md`, chapters
  });
}

// ---- build PATH_TO_INFO with prev/next threading ----
const info = {};
for (const s of series) {
  if (s.kind === 'page') {
    info[s.path] = { seriesId: s.id, seriesName: s.name, title: s.name, indexPath: s.path };
    continue;
  }
  s.chapters.forEach((ch, i) => {
    info[ch.path] = {
      seriesId: s.id, seriesName: s.name, edition: s.edition || undefined,
      title: ch.title, indexPath: s.index,
      prev: i > 0 ? s.chapters[i-1].path : undefined,
      next: i < s.chapters.length-1 ? s.chapters[i+1].path : undefined
    };
  });
}

const banner = `// manifest.js — GENERATED by tools/build-manifest.mjs. Do not hand-edit.\n`;
const out =
  banner +
  `window.SITE = ${JSON.stringify(SITE, null, 2)};\n\n` +
  `window.SERIES = ${JSON.stringify(series, null, 2)};\n\n` +
  `window.PATH_TO_INFO = ${JSON.stringify(info, null, 2)};\n`;

writeFileSync(join(ROOT, 'manifest.js'), out);
console.log(`manifest.js written: ${series.length} nav entries, ${Object.keys(info).length} pages.`);
